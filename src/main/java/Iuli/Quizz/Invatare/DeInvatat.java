public class DeInvatat {

    private int id;
    private String numeTara;
    private String capitala;
    private int idc;
    private String continent;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNumeTara() {
        return numeTara;
    }

    public void setNumeTara(String numeTara) {
        this.numeTara = numeTara;
    }

    public String getCapitala() {
        return capitala;
    }

    public void setCapitala(String capitala) {
        this.capitala = capitala;
    }

    public String getContinent() {
        return continent;
    }

    public int getIdc() {
        return idc;
    }

    public void setIdc(int idc) {
        this.idc = idc;
    }

    public void setContinent(String continent) {
        this.continent = continent;
    }

}

